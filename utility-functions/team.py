class Team:             
    def __init__(self, name, position, wins, draws, losses, gamesPlayed, goals, goalDiff, points, form):
        self.name = name
        self.position = position
        self.wins = wins
        self.losses = losses
        self.draws = draws
        self.gamesPlayed = gamesPlayed
        self.goals = goals
        self.goalDiff = goalDiff
        self.points = points
        self.form = form